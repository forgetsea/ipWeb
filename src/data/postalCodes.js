function getRegionLevel(code) {
  if (code.endsWith('0000')) return '省级'
  if (code.endsWith('00')) return '地市级'
  return '区县级'
}

export const postalCodeEntries = POSTAL_CODE_TEXT.split('\n')
  .map((line) => line.trim())
  .filter(Boolean)
  .map((line) => {
    const [code, name] = line.split(':')
    return {
      code,
      name,
      level: getRegionLevel(code),
    }
  })

export const provinceOptions = postalCodeEntries
  .filter((item) => item.code.endsWith('0000'))
  .map((item) => ({
    code: item.code,
    label: item.name.endsWith('省') ||
      item.name.endsWith('市') ||
      item.name.endsWith('自治区') ||
      item.name.endsWith('特别行政区')
      ? item.name
      : `${item.name}${item.name.length <= 4 ? '省' : ''}`,
  }))

export function getPostalCodeLabel(code) {
  return postalCodeEntries.find((item) => item.code === code)?.name || ''
}
